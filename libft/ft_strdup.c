#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	size_t	len;
	char	*result;

	i = 0;
	len = ft_strlen(str);
	if (!(result = (char *)malloc(sizeof(char) * len + 1)))
		return (NULL);
	while (str[i])
	{
		result[i] = str[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
