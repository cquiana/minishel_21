#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	result;

	if (!dst || !src)
		return (0);
	i = 0;
	result = ft_strlen(src);
	if (size == 0)
		return (result);
	while (src[i] != '\0' && i < (size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (result);
}
