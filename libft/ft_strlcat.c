#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	result;

	result = ft_strlen(src);
	if (size == 0)
		return (result);
	i = ft_strlen(dst);
	j = 0;
	if (size < i + 1)
		result += size;
	else
		result += i;
	while (src[j] != '\0' && i < (size - 1))
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (result);
}
